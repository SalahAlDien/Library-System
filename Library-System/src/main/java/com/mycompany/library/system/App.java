    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        BookArea.setText("");
        BookArea.append("The Following is All The Available Books" + "\n" + "================================" + "\n");
        
        boolean faild = true; 
         for(Book b : Lib.getBooks()){
            if(b.isAvailable()){
                BookArea.append(b.getInfo() + "\n" + "================================" + "\n");
                faild = false;
            }
        }
        for(Journal b : Lib.getJournals()){
            if(b.isAvailable()){
                BookArea.append(b.toString() + "\n" + "================================" + "\n");
                faild = false; 
            }
        }
        for(Magazine b : Lib.getMagazines()){
            if(b.isAvailable()){
                BookArea.append(b.toString() + "\n" + "================================" + "\n");
                faild = false;
            }
        }
        
        if(faild)
            JOptionPane.showMessageDialog(rootPane, "There is No Availabe Books", "Search Completed", WIDTH);
        else 
            JOptionPane.showMessageDialog(rootPane, "Finished Searching", "Search Completed", WIDTH);
        
    }//GEN-LAST:event_jButton11ActionPerformed

