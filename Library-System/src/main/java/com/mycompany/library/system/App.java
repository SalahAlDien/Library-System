    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int Id = Integer.parseInt(StudentId.getText()); 
        int bookNo = Integer.parseInt(BookNo.getText()); 


        Student s  = Lib.FindStudentById(Id); 
        Book b = Lib.SearchBookByNumber(bookNo); 
        
        if(s == null || b == null )
        {
            JOptionPane.showConfirmDialog(rootPane, "Student Doesn't Exist Faild to Loan");
            return ; 
        }
        
        Loan L = Lib.loanBook(s, b);    
        LoanArea.append(L.getInfo() + "\n"+ "================================" + "\n" );
        JOptionPane.showMessageDialog(rootPane, b.getTitle() + "Has been Loaned Successfully");
        
        StudentId.setText("");
        BookNo.setText("");
    }//GEN-LAST:event_jButton8ActionPerformed

